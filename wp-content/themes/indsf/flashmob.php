FLASH MOB À L’ÉCOLE SAINT FERDINAND

 

FlashMob4.pngLe samedi 14 décembre, l’école SAINT FERDINAND à CHARTRES a inauguré ses nouveaux locaux de maternelle.

Afin de présenter l'image de notre école, aux invités, le jour de l'inauguration, le flash mob s'est imposé : convivialité, unité et dynamisme.


Ce fut une belle aventure, le soir, à l’abri des regards indiscrets, les enseignants et le personnel, se retrouvaient dans la bonne humeur pour apprendre une chorégraphie inventée et des gestes symbolisant la vie d’un écolier en école catholique : réfléchir - se recueillir - avancer - écrire - parler - écouter - grandir et aimer. Puis, chaque jour les élèves ont à leur tour appris la chorégraphie dans le cadre d’une simple expression corporelle sans rien savoir de plus.  

Le jeudi 5 décembre, il règne une ambiance particulière Toutes les classes sont dans la cour de primaire, les enseignants et le personnel non enseignant sont tous là ; ils portent des gants blancs. Les enfants questionnent.

Tout à coup, ce sont  les premières notes de musique une énorme explosion de joie retentit, les caméramen, les photographes sortent de leur cachette.

Sans réfléchir et avec un immense plaisir les enfants se mettent à danser les pas qu'ils connaissent désormais par cœur  autour de tous les adultes de l'école. On ne s'en lasse pas, le flash mob est dansé 4 fois et les enfants crient "encore, encore ..." Personne n'a envie d'arrêter ce moment de partage unique

FlashMob1.pngFlashMob2.pngFlashMob3.png

 
De retour en classe, l'excitation est réelle et saine, chacun avec des étoiles dans les yeux heureux d'avoir vécu en symbiose le temps d'une danse.

Ce qu'ils ont préféré ? C'est que tout le monde soit réuni, toute l'école.

Ce jour là, les sourires sont restés gravés sur les visages toute la journée.

Nous danserons encore le jour des portes ouvertes, et sur la place des Epars pour Carnaval !

